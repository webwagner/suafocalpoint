        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Quem pode ver meus imóveis?</h3>
        Somente Parceiros que você autorizou podem ver seus imóveis, apenas você poderá ver as Informações Privadas de cada imóvel. Casa não queira mais que algum Parceiro visualize seus imóveis você pode cancelar sua assinatura.

        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Como vejo imóveis de outros corretores?</h3>
        Assim como seus Parceiros, você precisará ser autorizado para visualizar os imóveis de outros corretores. Caso não queira mais visualizar algum Parceiro você pode cancelar sua assinatura.

        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Quem verá quando eu adiciono um novo imóvel?</h3>
        Todos os seus Parceiros serão informados por e-mail que um novo imóvel foi inserido em seu cadastro. Assim como você será informado quando algum Parceiro inserir um imóvel.

        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Como posso expandir minha rede?</h3>
        Existem duas maneiras para você expandir sua rede, você pode adicionar corretores que já são usuarios da Sua Focal Point, ou poderá convidar corretores que ainda não usam o serviço, para isso clique em "Convide seu corretor" no menu inferior de cada página e receba um desconto de 50% por um mês. E lembre-se, para cada indicação você recebe 50% de desconto!

        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Eu tenho que adicionar fotos?</h3>
        Você não precisa por fotos, mas é recomendáve! Você pode por fotos pelo computador, pelo celular ou por um tablet.

        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Eu tenho que preencher as informações dos proprietários?</h3>
        Não, as informações do proprietário são apenas para sua conviniência, ninguém além de você terá acesso à elas.

        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Se eu divido a comissão com alguém, quem é responsável pela garantia que serei pago?</h3>
        Você mesmo! O site é apenas uma ferramenta, você será responsável pela sua rede e suas parcerias e a forma de como negociar com elas.

        <h3 style="margin: 10px 0 5px 0; float: left; width: 100%;">Vai Funcionar no meu iPhone? iPad? Ou Android? </h3>
        Sua Focal Point funciona em Iphones, iPads, e qualquer aparelho com sistema Android.
