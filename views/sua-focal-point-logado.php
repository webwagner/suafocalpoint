        <h1 style="margin: 20px 0 10px 0; float: left; width: 100%;">A Rede Social dos Corretores de Imóveis</h1>
        Sua Focal Point é uma ferramenta on-line feita especialmente para Corretores de imóveis, que irá lhe proporcionar a possibilidade de ter um maior número de imóveis para vender ou alugar, com isso aumentando a chance de fechar bons negócios. Você poderá também organizar a sua lista de imóveis de maneira muito mais produtiva e profissional.

        <h3 style="margin: 10px 0 5px 0;">Você poderá:</h3>
        Obter acesso a mais imóveis exclusivos<br />
        Publicar sua lista de imóveis com mais facilidade e rapidez<br />
        Compartilhar sua lista de imóveis com seus parceiros exclusivos escolhidos por você<br />
        Ter mais tempo para dedicar com cada cliente em potencial<br />
        Mostrar sua rede exclusiva de corretores aos proprietários dos imóveis<br />
        Economizar tempo e ganhar mais dinheiro.

        <h3 style="margin: 10px 0 5px 0;">Vantagens para Vendedores e Locadores</h3>
        Ter seu imóvel disponível para um maior número de corretores de maneira mais rápida e eficiente<br />
        Lidar com apenas um corretor que use Sua Focal Point, pois ele poderá compartilhar seu imóvel com muitos outros.<br />
        Ter mais controle no processo de locação ou venda de seu imóvel<br />
        Ter um maior número de candidatos reais que visitem seu imóvel<br />
        Ter mais tranquilidade<br />
        Ver que os corretores imobiliários estão sendo encorajados a trabalhar de maneira mais transparente e profissional

        <h3 style="margin: 10px 0 5px 0;"> Vantagens para Compradores e Locatários</h3>
        Visitar um maior número de imóveis dentro do perfil que você procura.<br />
        Economizar bastante tempo<br />
        Decidir qual corretor será seu representante<br />
        Evitar trabalhar com corretores que não tenham uma boa rede.<br />
        Encorajar os corretores a trabalhar de maneira mais transparente e profissional.<br />
