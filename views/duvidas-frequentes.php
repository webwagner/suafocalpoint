        <h1>Dúvidas Frequentes</h1>

        <h3>Quem pode ver meus imóveis?</h3>
        Somente Parceiros que você autorizou podem ver seus imóveis, apenas você poderá ver as Informações Privadas de cada imóvel. Caso não queira mais que algum Parceiro visualize seus imóveis, você pode excluí-lo de sua lista de Parceiros.
        
        <h3>Como vejo imóveis de outros corretores?</h3>
        Assim como seus Parceiros, você precisará ser autorizado para visualizar os imóveis de outros corretores. Caso não queira mais visualizar algum Parceiro, você pode excluí-lo de sua lista de Parceiros..
       
        <h3>Quem verá quando eu adicionar um novo imóvel?</h3>
        Todos os seus Parceiros serão informados por e-mail que um novo imóvel foi inserido em seu cadastro. Assim como você será informado quando algum Parceiro inserir um imóvel.
        
        <h3>Como posso expandir minha rede?</h3>
        Existem duas maneiras para você expandir sua rede: você pode adicionar corretores que já são usuários da Sua Focal Point ou poderá convidar corretores que ainda não usam o serviço. Para isso, clique em ?Convide seu corretor? no menu inferior de cada página e receba um desconto de 50% por um mês. E lembre-se, para cada indicação você recebe 50% de desconto!
       
        <h3>Eu tenho que adicionar fotos?</h3>
        Você não precisa inserir fotos, mas é recomendável! Você pode adicionar fotos pelo computador, celular ou por um tablet.
        (Você poderá inserir até 10 fotos, de até 2MB cada uma, no formato JPG)

        <h3>Eu tenho que preencher as informações dos proprietários?</h3>
        Não, as informações do proprietário são apenas para sua conveniência. Ninguém além de você terá acesso a elas.
        
        <h3>Se eu divido a comissão com alguém, quem é responsável pela garantia de que serei pago?</h3>
        Você mesmo! O site é apenas uma ferramenta, você será responsável pela sua rede e suas parcerias e a forma de como negociar com elas.

        <h3>Vai Funcionar no meu iPhone? iPad? Ou Android? </h3>
        Sua Focal Point funciona em Iphones, iPads, e qualquer aparelho com sistema Android.

        <h3>Como retiro um imóvel do mercado?</h3>
        Para retirar um imóvel do mercado, basta clicar em EDITAR, selecionar INFORMAÇÕES PRIVADAS no menu principal da esquerda e inserir uma data no campo VENCIMENTO DE CONTRATO.        
        OBS: Esta data não precisa ser a data de vencimento do contrato, mas uma data em que você gostaria de ser lembrado deste imóvel.
        
        <h3>Como retorno um imóvel ao mercado?</h3>
        Para retornar um imóvel, clique em EDITAR, em seguida em INFORMAÇÕES PRIVADAS e simplesmente apague a data de VENCIMENTO DE CONTRATO.
        
        <h3>Onde ficam meus imóveis vencidos?</h3>
        Seus imóveis vencidos podem ser localizados em LEMBRETE DE IMÓVEIS, no menu principal à esquerda.
